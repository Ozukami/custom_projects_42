#include "libft.h"

void	ft_putnbr_u_int(unsigned int n)
{
	if (n < 10)
		ft_putchar('0' + n);
	else
	{
		ft_putnbr_u_int(n / 10);
		ft_putchar('0' + n % 10);
	}
}
