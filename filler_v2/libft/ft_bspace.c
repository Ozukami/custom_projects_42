#include "libft.h"

void	ft_bspace(char *s, size_t n)
{
	size_t	i;

	i = 0;
	if (n == 0)
		return ;
	while (i < n - 1)
	{
		s[i] = ' ';
		i++;
	}
	s[i] = 0;
}
